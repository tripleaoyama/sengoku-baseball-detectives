export function RubyText({ text }: RubyTextProps) {
  return <>{renderRuby(String(text))}</>;
}

function renderRuby(text: string) {
  const nodes: ReactNode[] = [];
  let index = 0;

  while (index < text.length) {
    const entry = rubyEntries.find(([word]) => text.startsWith(word, index));
    if (!entry) {
      nodes.push(text[index]);
      index += 1;
      continue;
    }

    const [word, reading] = entry;
    nodes.push(
      <ruby key={`${word}-${index}`}>
        {word}
        <rt>{reading}</rt>
      </ruby>
    );
    index += word.length;
  }

  return nodes;
}
